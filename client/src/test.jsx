import React, { useState } from "react";
import { useDispatch } from 'react-redux';
import { ChangePriceCoins } from './Redux/action';

const Test = () => {
  const dispatch = useDispatch();
  const [coin, setCoin] = useState("");
  const [price, setPrice] = useState("");

  const handleCoinChange = (event) => {
    setCoin(event.target.value);
  };

  const handlePriceChange = (event) => {
    setPrice(event.target.value);
  };

  const handleSubmit = (event) => {
    event.preventDefault();
    // Aquí puedes realizar acciones con la moneda y el precio, como enviarlos a tu backend
    dispatch(ChangePriceCoins(coin, price)); // Corregí el envío de parámetros
  
    // También podrías despachar una acción de Redux aquí si es necesario
  };

  return (
    <form onSubmit={handleSubmit}>
      <div>
        <label htmlFor="coin">Moneda:</label>
        <input
          type="text"
          id="coin"
          value={coin}
          onChange={handleCoinChange}
          required
        />
      </div>
      <div>
        <label htmlFor="price">Precio:</label>
        <input
          type="number"
          id="price"
          value={price}
          onChange={handlePriceChange}
          required
        />
      </div>
      <button type="submit">Guardar</button>
    </form>
  );
};

export default Test;


